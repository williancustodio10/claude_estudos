numeros = [1, 2, 3, 4, 5]
nomes = ["Alice", "Willian", "Charlie", "David", "Eve"]
print("Números:", numeros)
print("Nomes:", nomes)

# Acessando elementos de uma lista
print("O primeiro número é:", numeros[0]) # Acessa o primeiro elemento da lista de números
print("O segundo nome é:", nomes[1]) # Acessa o segundo elemento da lista de nomes

# Modificando elementos de uma lista
numeros[0] = 10 # Modifica o primeiro elemento da lista de números
nomes[1] = "Beatriz" # Modifica o segundo elemento da lista de nomes
print("Números após modificação:", numeros)
print("Nomes após modificação:", nomes)

